package evaluation;

public class Livello {

	private int livello;
	private int point;
	public int getLivello() {
		return livello;
	}
	public void setLivello(int livello) {
		this.livello = livello;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public Livello(int livello, int point) {
		super();
		this.livello = livello;
		this.point = point;
	}
	
	
}
