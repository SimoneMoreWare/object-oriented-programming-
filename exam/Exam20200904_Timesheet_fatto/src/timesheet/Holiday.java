package timesheet;

public class Holiday {

	private int day;
	private int weekDay;

	public Holiday(int day) {
		super();
		this.day = day;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getWeekDay() {
		return weekDay;
	}

	public void setWeekDay(int weekDay) {
		this.weekDay = weekDay;
	}
	
}
